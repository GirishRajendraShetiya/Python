# pip(Pip Installs Packages) - used to install packages in Python.

# Commands:

# pip install <package_name>: To install the latest package.
# pip –version: To get the version installed on the system.
# pip list: To get the list of packages installed
# pip uninstall <package_name>: To uninstall a package.
# pip install <package_name>==version: To install a specific version as per the requirement.
# pip freeze > requirements.txt: To create the requirements.txt file
# pip freeze | xargs pip uninstall -y: To uninstall all the installed packages.
# pip install -r requirements.txt: To install a bunch of packages (provide the path of the .txt file if not in the same directory)

